            if(listagemItensSelectedRow < 0) {
                throw new Exception("Selecionar um item da Tabela");
            }
            int id = (int) this.listComputersTable.getValueAt(listagemItensSelectedRow, 0);
            this.deleteComputerService.execute(id);
            
            JOptionPane.showMessageDialog(null, "Item removido com sucesso");
            
